    public static void fillRadioButtons()
    {
        Connection con = null;
        con = SQLConnection.getSQLConnection();
        ResultSet rs;
        String datos[] = new String[6];
        TodosPacientes tP = new TodosPacientes();
        try{
        PreparedStatement ps = (PreparedStatement) con.prepareStatement("SELECT examen_fisico.* from examen_fisico WHERE examen_fisico.Id_exFi = ?");
        ps.setString(1, tP.getID());
        rs = ps.executeQuery();
        while(rs.next()){         
             datos[0]=rs.getString("Cardiovascular");
             datos[1]=rs.getString("Respiratorio");
             datos[2]=rs.getString("Gastrointestinal");
             datos[3]=rs.getString("Genito_urinario");
             datos[4]=rs.getString("Musculo_esqueletico");
             datos[5]=rs.getString("Piel");
        }
        con.close();
        } catch (SQLException e) {
            System.err.println(e);
        }
        if("Si".equals(datos[0])) { cardioSiRB.setSelected(true); }
        else { cardioNoRB.setSelected(true); }
        if("Si".equals(datos[1])) { respiSiRB.setSelected(true); }
        else { respiNoRB.setSelected(true); }
        if("Si".equals(datos[2])) { gastroSiRB.setSelected(true); }
        else { gastroNoRB.setSelected(true); }
        if("Si".equals(datos[3])) { geniSiRB.setSelected(true); }
        else { geniNoRB.setSelected(true); }
        if("Si".equals(datos[4])) { muscuSiRB.setSelected(true); }
        else { muscuNoRB.setSelected(true); }
        if("Si".equals(datos[5])) { pielSiRB.setSelected(true); }
        else { pielNoRB.setSelected(true); }
    }
    
    
