    public static void enableBtnSiguiente()
    {
        if((cardioSiRB.isSelected() || cardioNoRB.isSelected()) &&
                (respiSiRB.isSelected() || respiNoRB.isSelected()) &&
                (gastroSiRB.isSelected() || gastroNoRB.isSelected()) &&
                (geniSiRB.isSelected() || geniNoRB.isSelected()) &&
                (muscuSiRB.isSelected() || muscuNoRB.isSelected()) &&
                (pielSiRB.isSelected() || pielNoRB.isSelected()))
        {
            siguienteButton.setEnabled(true);
        }
        else
        {
            siguienteButton.setEnabled(false);
        }
    }
    
